#include <cmath>
#include <iostream>

int main() {
    std::cout << "9 + 1 = " << (9 + 1) << std::endl;
    std::cout << "9 * 2 = " << (9 * 2) << std::endl;
    std::cout << "8 / 2 = " << (9 / 2) << std::endl;
    std::cout << "2^3 = " << pow(2, 3) << std::endl;
    std::cout << "Square root of 16 = " << sqrt(16) << std::endl;
}
